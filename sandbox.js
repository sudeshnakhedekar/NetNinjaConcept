//switch staements
const grade ='A';

switch(grade){
    case 'A':
        console.log("you got an A! grade");
        break;
    case 'B':
        console.log("you got an B! grade");
        break;
    case 'C':
        console.log("you got an C! grade");
        break;
    case 'D':
        console.log("you got an D! grade");
        break; 
    case 'E':
        console.log("you got an E! grade");
        break; 
    default:
        console.log('not a valid grade!');      
}





