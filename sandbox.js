let age = 25;

//loose comaparsion (different types can still be equal)
 console.log(age == 25);
 console.log(age =='25');

 console.log(age != 25);
 console.log(age !='25');


 //strict comparsion (different types cannot be equal)
 
 console.log(age === 25);
 console.log(age ==='25');

 console.log(age !== 25);
 console.log(age !=='25');